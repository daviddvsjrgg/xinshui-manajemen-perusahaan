    public void tampilPerusahaan() {
        
        if ("Super Admin".equals(hakAksesAdminSET) || "Admin".equals(hakAksesAdminSET)){
            try {
            
            DefaultTableModel tblModel = new DefaultTableModel();
                tblModel.addColumn("ID Perusahaan");
                tblModel.addColumn("Nama Perusahaan");
            //Tampil Data...
            String sql = "{ call tampilPerusahaan() }";
            
            stat = con.createStatement();
            ResultSet rs = stat.executeQuery(sql);
           
            while(rs.next()){
                tblModel.addRow(new Object[]
                            {
                                rs.getString(1),
                                rs.getString(2)
                            }
                        );
                
            }
            tabelPerusahaan.setModel(tblModel);
        } catch (SQLException e) {   
                
        }
        }
        
    }

