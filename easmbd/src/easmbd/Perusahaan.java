    private void tambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tambahActionPerformed
        // TODO add your handling code here:
       ubah.setEnabled(false);
       hapus.setEnabled(false);
        try {
            stat = con.createStatement();
            String SQL = "INSERT INTO namaperusahaan VALUES('"+idPerusahaan.getText()+"','"+namaPerusahaan.getText()+"')";
            stat.executeUpdate(SQL);
            tampilPerusahaan();
            clear();
            JOptionPane.showMessageDialog(null, "Berhasil disimpan");
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Data gagal dimasukkan");
        }
    }//GEN-LAST:event_tambahActionPerformed

    private void ubahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ubahActionPerformed
        // TODO add your handling code here:
        try {
            String sql ="UPDATE namaperusahaan SET IDPERUSAHAAN = '"+idPerusahaan.getText()+"', NAMAPERUSAHAAN = '"+namaPerusahaan.getText()+"' WHERE IDPERUSAHAAN = '"+idPerusahaan.getText()+"'";
            pstat = con.prepareStatement(sql);
            pstat.execute();
            JOptionPane.showMessageDialog(null, "Data berhasil diubah");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Perubahan Data Gagal"+e.getMessage());
        }
        tampilPerusahaan();
        clear();
    }//GEN-LAST:event_ubahActionPerformed

    private void tabelPerusahaanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelPerusahaanMouseClicked
        // TODO add your handling code here:
        tambah.setEnabled(false);
        ubah.setEnabled(true);
        hapus.setEnabled(true);
        idPerusahaan.setEnabled(false);
        try {
            int baris = tabelPerusahaan.rowAtPoint(evt.getPoint());
            String nama1 = tabelPerusahaan.getValueAt(baris, 0).toString();
            idPerusahaan.setText(nama1);
            String nama2 = tabelPerusahaan.getValueAt(baris, 1).toString();
            namaPerusahaan.setText(nama2);
        } catch (Exception e) {
        }
    }//GEN-LAST:event_tabelPerusahaanMouseClicked

    private void hapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hapusActionPerformed
        // TODO add your handling code here:
          try {
            int selectedOption = JOptionPane.showConfirmDialog(null, 
                                  "Yakin ingin dihapus?", 
                                  "Hapus Data", 
                                  JOptionPane.YES_NO_OPTION); 
        if (selectedOption == JOptionPane.YES_OPTION) {
             String sql ="DELETE FROM namaperusahaan WHERE IDPERUSAHAAN = '"+idPerusahaan.getText()+"'";
             pstat = con.prepareStatement(sql);
             pstat.execute();
             JOptionPane.showMessageDialog(null, "Data berhasil dihapus");
             tampilPerusahaan();
             clear();
        }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Perubahan Data Gagal"+e.getMessage());
        }
    }//GEN-LAST:event_hapusActionPerformed

